package com.funmix.model;


public class Phone {
	String imei;
	int phone;
	int sdk;
	int ver;
	
	public String getImei() {
		return imei;
	}
	
	public void setImei(String imei) {
		this.imei = imei;
	}
	
	public int getPhone() {
		return phone;
	}
	
	public void setPhone(int phone) {
		this.phone = phone;
	}
	
	public int getSdk() {
		return sdk;
	}
	
	public void setSdk(int sdk) {
		this.sdk = sdk;
	}
	
	public int getVer() {
		return ver;
	}
	
	public void setVer(int ver) {
		this.ver = ver;
	}
	
}
